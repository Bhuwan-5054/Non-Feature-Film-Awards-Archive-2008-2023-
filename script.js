// ===== AUTO YEARS (NO NEED TO EDIT) =====
const yearsList = Object.keys(data);

// ===== DOM ELEMENTS =====
const yearsDiv = document.getElementById("years");
const contentDiv = document.getElementById("content");

// ===== LOAD YEARS =====
yearsList.forEach(year => {
  const card = document.createElement("div");
  card.className = "card";
  card.innerText = year;

  card.onclick = () => showYear(year);

  yearsDiv.appendChild(card);
});

// ===== SHOW YEAR DATA =====
function showYear(year) {
  contentDiv.style.display = "block";

  contentDiv.innerHTML = `
    <h2>${year} Awards</h2>
  `;

  data[year].forEach(item => {
    const div = document.createElement("div");
    div.className = "item";

    div.innerHTML = `
      <h3>${item.category}</h3>
      <button class="link-btn" onclick="window.open('${item.file}', '_blank')">
      View Details
      </button>
    `;

    contentDiv.appendChild(div);
  });

  window.scrollTo({
    top: contentDiv.offsetTop,
    behavior: "smooth"
  });
}

// ===== TOGGLE SYNOPSIS =====
function toggleSynopsis(btn) {
  const text = btn.nextElementSibling;
  text.classList.toggle("hidden");
}

// ===== SEARCH FUNCTION =====
document.getElementById("search").addEventListener("input", function () {
  const value = this.value.toLowerCase();

  document.querySelectorAll(".item").forEach(item => {
    const text = item.innerText.toLowerCase();
    item.style.display = text.includes(value) ? "block" : "none";
  });
});
